public class ComparisonOperation {
    public static void main(String[] args) {
        int valueA = 10;
        int valueB = 20;

        System.out.println(valueA > valueB);
        System.out.println(valueA < valueB);
        System.out.println(valueA >= valueB);
        System.out.println(valueA <= valueB);
        System.out.println(valueA == valueB);
        System.out.println(valueA != valueB);

    }
}
